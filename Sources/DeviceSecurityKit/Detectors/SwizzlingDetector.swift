           let metaCls = object_getClass(cls) {
            let sel = NSSelectorFromString(connSel)
            if class_getInstanceMethod(metaCls, sel) != nil,
               let imp = class_getMethodImplementation(metaCls, sel) {
                if impIsOutsideSystem(imp, systemPrefixes: systemPrefixes, label: "+\(connClass).\(connSel)") {
                    return true
                }
            }
        }

        return false
    }

    private static func impIsOutsideSystem(
        _ imp: IMP,
        systemPrefixes: [String],
        label: String
    ) -> Bool {
        let ptr = unsafeBitCast(imp, to: UnsafeRawPointer.self)
        var info = Dl_info()
        guard dladdr(ptr, &info) != 0, let fname = info.dli_fname else {
            logger.warning("Swizzle check: could not resolve image for \(label)")
            return true
        }
        let imagePath = String(cString: fname)
        if !systemPrefixes.contains(where: { imagePath.hasPrefix($0) }) {
            logger.warning("Method swizzling detected: \(label) → \(imagePath)")
            return true
        }
        return false
    }

    // MARK: - Check 2: Known swizzling libraries

    private static func checkKnownSwizzlingLibraries() -> Bool {
        let suspiciousLibraries = [
            o.reveal([0xC6, 0xC3, 0xC8, 0xCB, 0xD9, 0xDA, 0xCF, 0xC9, 0xDE, 0xD9]), // libaspects
            o.reveal([0xD8, 0xD9, 0xD9, 0xDD, 0xC3, 0xD0, 0xD0, 0xC6, 0xCF]),        // rsswizzle
            o.reveal([0xC0, 0xD8, 0xD9, 0xDD, 0xC3, 0xD0, 0xD0, 0xC6, 0xCF]),        // jrswizzle
        ]

        let imageCount = _dyld_image_count()
        for i in 0..<imageCount {
            guard let rawName = _dyld_get_image_name(i) else { continue }
            let imageName = String(cString: rawName).lowercased()
            for lib in suspiciousLibraries {
                if imageName.contains(lib) {
                    logger.warning("Known swizzling library detected: \(imageName)")
                    return true
                }
            }
        }

        return false
    }
}
