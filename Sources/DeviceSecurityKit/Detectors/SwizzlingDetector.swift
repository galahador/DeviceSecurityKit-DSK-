            (
                [0xE4, 0xF9, 0xEC, 0xC3, 0xC6, 0xCF, 0xE7, 0xCB, 0xC4, 0xCB, 0xCD, 0xCF, 0xD8],
                [0xCC, 0xC3, 0xC6, 0xCF, 0xEF, 0xD2, 0xC3, 0xD9, 0xDE, 0xD9, 0xEB, 0xDE,
