import Foundation
import Darwin
import MachO
import ObjectiveC

public final class SwizzlingDetector {

    private static let logger = SecurityLogger.security(subsystem: "SwizzlingDetector")
    private static let o = StringObfuscator.shared

    public static func isSwizzled() -> Bool {
        return checkSystemMethodOrigins()
            || checkKnownSwizzlingLibraries()
    }

    // MARK: - Check 1: Method IMP origin

    private static func checkSystemMethodOrigins() -> Bool {
        let systemPrefixes = [
            o.reveal([0x85, 0xDF, 0xD9, 0xD8, 0x85, 0xC6, 0xC3, 0xC8, 0x85]),
            o.reveal([0x85, 0xF9, 0xD3, 0xD9, 0xDE, 0xCF, 0xC7, 0x85, 0xE6, 0xC3, 0xC8, 0xD8, 0xCB, 0xD8, 0xD3, 0x85]),
            o.reveal([0x85, 0xE6, 0xC3, 0xC8, 0xD8, 0xCB, 0xD8, 0xD3, 0x85, 0xEB, 0xDA, 0xDA, 0xC6, 0xCF, 0x85]),
        ]

        let instanceTargets: [([UInt8], [UInt8])] = [
            (
                [0xE4, 0xF9, 0xFF, 0xF8, 0xE6, 0xF9, 0xCF, 0xD9, 0xD9, 0xC3, 0xC5, 0xC4],
                [0xCE, 0xCB, 0xDE, 0xCB, 0xFE, 0xCB, 0xD9, 0xC1, 0xFD, 0xC3, 0xDE, 0xC2,
                 0xF8, 0xCF, 0xDB, 0xDF, 0xCF, 0xD9, 0xDE, 0x90, 0xC9, 0xC5, 0xC7, 0xDA,
                 0xC6, 0xCF, 0xDE, 0xC3, 0xC5, 0xC4, 0xE2, 0xCB, 0xC4, 0xCE, 0xC6, 0xCF,
                 0xD8, 0x90]
            ),
            (
                [0xE4, 0xF9, 0xFF, 0xF8, 0xE6, 0xF9, 0xCF, 0xD9, 0xD9, 0xC3, 0xC5, 0xC4],
                [0xDF, 0xDA, 0xC6, 0xC5, 0xCB, 0xCE, 0xFE, 0xCB, 0xD9, 0xC1, 0xFD, 0xC3,
                 0xDE, 0xC2, 0xF8, 0xCF, 0xDB, 0xDF, 0xCF, 0xD9, 0xDE, 0x90, 0xCC, 0xD8,
                 0xC5, 0xC7, 0xEE, 0xCB, 0xDE, 0xCB, 0x90, 0xC9, 0xC5, 0xC7, 0xDA, 0xC6,
                 0xCF, 0xDE, 0xC3, 0xC5, 0xC4, 0xE2, 0xCB, 0xC4, 0xCE, 0xC6, 0xCF, 0xD8,
                 0x90]
            ),
            (
                [0xFF, 0xE3, 0xEB, 0xDA, 0xDA, 0xC6, 0xC3, 0xC9, 0xCB, 0xDE, 0xC3, 0xC5, 0xC4],
                [0xC9, 0xCB, 0xC4, 0xE5, 0xDA, 0xCF, 0xC4, 0xFF, 0xF8, 0xE6, 0x90]
            ),
            (
                [0xE4, 0xF9, 0xE5, 0xC8, 0xC0, 0xCF, 0xC9, 0xDE],
                [0xCC, 0xC5, 0xD8, 0xDD, 0xCB, 0xD8, 0xCE, 0xE3, 0xC4, 0xDC, 0xC5, 0xC9,
                 0xCB, 0xDE, 0xC3, 0xC5, 0xC4, 0x90]
            ),
            // NSURLSession – dataTaskWithURL:completionHandler:
            (
                [0xE4, 0xF9, 0xFF, 0xF8, 0xE6, 0xF9, 0xCF, 0xD9, 0xD9, 0xC3, 0xC5, 0xC4],
                [0xCE, 0xCB, 0xDE, 0xCB, 0xFE, 0xCB, 0xD9, 0xC1, 0xFD, 0xC3, 0xDE, 0xC2,
